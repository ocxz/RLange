# 常用数学函数
# 1、计算绝对值：abs(x)
# 2、开平方跟：sqrt(x)   sqrt(25)  --> 25^(0.5) 等价
# 3、向大取整：ceilling(x)
# 4、向小取整：floor(x)
# 5、截取整数：trunc(x)
# 6、四舍五入：round(x, digits=n)  digits指定保留小数位数
# 7、有效数：signif(x, digits=n)   四舍五入保留，保留n位有效数字
# 8、三角函数：cos(x)、sin(x)、tan(x)
# 9、反三角函数：acos(x)、asin(x)、atan(x)
# 10、双曲函数：cosh(x)、sinh(x)、tanh(x)
# 11、反双曲函数：acosh(x)、asinh(x)、atanh(x)
# 12、对数函数：log(x, base=n)    对x取以n为底的对数
# 13、自然对数：log(x)   以自然数e为底数
# 14、常用对数：log10(x)  以10为底数
# 15、指数函数：exp(x)    即e的x次方