# 回归分析的各种变体
# 1、简单的线性回归：用一个自变量 预测 一个因变量
# 2、多项式：用一个自变量 预测 一个因变量，但模型关系是n阶多项式
# 3、多元线性：多个自变量 预测 一个因变量
# 4、多变量：一个或多个自变量 预测 多个因变量
# 5、Logistic：一个或多个自变量 预测 一个类别型因变量
# 6、泊松：一个或多个自变量 预测 一个频数因变量
# 7、Cox比例风险：一个或多个自变量 预测 一个时间（死亡、失败或旧病复发）发生的时间
# 8、时间序列：对误差项相关的时间序列数据建模
# 9、非线性：用一个或多个自变量 预测 一个因变量，但其模型是非线性的
# 10、非参数：用一个或多个自变量 预测 一个因变量，其实模型的形式源于数据形式，不做事先设定
# 11、稳健：一个或多个变量 预测 一个因变量，能抵御强影响点的干扰

